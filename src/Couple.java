public class Couple {
    int disposant;
    int proposant;
    public Couple(int disposant,int proposant)
    {
        this.disposant=disposant;
        this.proposant=proposant;
    }

    public int getDisposant() {
        return disposant;
    }

    public void setDisposant(int disposant) {
        this.disposant = disposant;
    }

    public int getProposant() {
        return proposant;
    }

    public void setProposant(int proposant) {
        this.proposant = proposant;
    }
}
